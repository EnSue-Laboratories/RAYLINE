// Current language — module-level singleton, updated by LanguageContext.
let _lang = "en";

export function setLang(lang) {
  _lang = lang === "zh" ? "zh" : "en";
}

export function getLang() {
  return _lang;
}

/**
 * Translate a key. If the value is a function, call it with ...args.
 * Falls back to the English string, then the raw key.
 */
export function t(key, ...args) {
  const dict = translations[_lang] || translations.en;
  const val = dict[key] ?? translations.en[key] ?? key;
  return typeof val === "function" ? val(...args) : val;
}
